<h2>Tema s težo</h2>
Urejanje člankov v temi s težo. To so članki, ki jih pišejo izbrani content editorji in so daljši. Gre za spletni časopis: standardni članki s kategorizacijo, sorodnimi vsebinami, komentarji, multimedijo. Članki so urejeni po avtorjih, ki imajo od tega "credit", bralci jih radi/ne berejo in zaradi njih pridejo na sajt. Bralci si lahko njihov "feed" dodajo v newsletter, ki ga dobijo tedensko/mesečno/on_updates_available. 


